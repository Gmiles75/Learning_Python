# Teach you how to assign multiple values to multiple variales on the same line

title, author, year, description = 'Jazz', 'Toni Morrison', 1993, 'Jazz Era'
print('Book Title: ', title)
print('Author Name:', author)
print('Publishing Year:', year)
print('Description:', description)

style, designer, size = 'loafer', 'Johnston & Murphy', 8.5
print('Shoe Style;', style)
print('Shoe Designer:', designer)
print('Shoe Size:', size)